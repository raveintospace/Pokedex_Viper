import UIKit

extension UILabel {
    func applyVerdanaBoldAndSize16() {
        font = UIFont(name: "Verdana-Bold", size: 16)
    }
    
    func applyVerdanaAndSize14() {
        font = UIFont(name: "Verdana", size: 14)
    }
}
